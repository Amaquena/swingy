package swingy.model.artifacts.weapons;

public abstract class Weapon {
	private String name;
	private int attack;

	public Weapon(String name) {
		this.setName(name);
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getAttack() {
		return attack;
	}

	public void setAttack(int attack) {
		this.attack = attack;
	}
}
